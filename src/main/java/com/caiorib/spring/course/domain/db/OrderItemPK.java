package com.caiorib.spring.course.domain.db;

import com.caiorib.spring.course.domain.OrderEntity;
import com.caiorib.spring.course.domain.ProductEntity;

import javax.persistence.Embeddable;
import javax.persistence.ForeignKey;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import java.io.Serializable;
import java.util.Objects;

@Embeddable
public class OrderItemPK implements Serializable {
    private static final long serialVersionUID = 1L;

    @ManyToOne
    @JoinColumn(name = "IDT_ORDER", foreignKey = @ForeignKey(name = "ORDER_FK"))
    private OrderEntity order;

    @ManyToOne
    @JoinColumn(name = "IDT_PRODUCT", foreignKey = @ForeignKey(name = "PRODUCT_FK"))
    private ProductEntity product;

    public OrderEntity getOrder() {
        return order;
    }

    public void setOrder(OrderEntity order) {
        this.order = order;
    }

    public ProductEntity getProduct() {
        return product;
    }

    public void setProduct(ProductEntity product) {
        this.product = product;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        OrderItemPK that = (OrderItemPK) o;
        return Objects.equals(order, that.order) && Objects.equals(product, that.product);
    }

    @Override
    public int hashCode() {
        return Objects.hash(order, product);
    }
}
